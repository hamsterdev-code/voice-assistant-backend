def get_analysis_prompt(cargo_results: list) -> str:
    """Генерирует промпт для анализа результатов поиска"""
    return f"""Проанализируй найденные грузы и выдели ТОП-3 лучших по критериям:
- Цена за км (выше = лучше)
- Маржинальность (выше = лучше)
- Расстояние и удобство маршрута

Найдено грузов: {len(cargo_results)}

Данные грузов:
{cargo_results}

Верни анализ в формате:
- Топ-3 ID грузов
- Краткое резюме (2-3 предложения)
- Рекомендации водителю
"""
